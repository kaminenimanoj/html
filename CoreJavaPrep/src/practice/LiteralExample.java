package practice;

public class LiteralExample {

		    public static void main(String[] args)
		    {
		          // decimal-form literal
		        int a = 101; 
		          // octal-form literal
		        int b = 0100; 
		        // Hexa-decimal form literal
		        int c = 0xFace; 
		        
		        System.out.println(a);
		        System.out.println(b);
		        System.out.println(c);
		        
		    }
}
		
